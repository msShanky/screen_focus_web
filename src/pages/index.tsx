import { Grid, Text } from "@mantine/core";
import { AnimatePresence, motion } from "framer-motion";
import type { NextPage } from "next";

import Head from "next/head";
import Link from "next/link";

const list = {
	visible: {
		opacity: 1,
		transition: {
			when: "beforeChildren",
			staggerChildren: 0.4,
		},
	},
	hidden: {
		opacity: 0,
		transition: {
			when: "afterChildren",
		},
	},
};

const item = {
	visible: { opacity: 1, x: 0, y: 0 },
	hidden: { opacity: 0, x: 0, y: -400 },
};

const pageLinks = [
	{
		link: "/post-production",
		text: "Post Production Hub",
	},
	{
		link: "/camera-rental",
		text: "Camera Rental",
	},
	{
		link: "/production",
		text: "Production",
	},
	{
		link: "/interiors",
		text: "Home Theater & Studio Interiors",
	},
	{
		link: "http://premiumfreshfoods.com/sicf-1/",
		text: "Film Festival & Awards",
	},
];

const Home: NextPage = () => {
	const MotionGrid = motion(Grid, { forwardMotionProps: true });
	const MotionCol = motion(Grid.Col, { forwardMotionProps: true });
	return (
		<>
			<Head>
				<title>ScreenFocus | Home</title>
				<meta name="description" content="Generated by create next app" />
				<link rel="icon" href="/favicon.ico" />
			</Head>
			<AnimatePresence>
				<MotionGrid initial="hidden" animate="visible" variants={list} className="mt-20 select-none justify-evenly">
					{pageLinks.map((page) => {
						const key = page.text;
						const target = page.link.includes("http") ? "_blank" : "_self";
						console.log("THE TARGET IS", target);
						return (
							<Link key={key} href={page.link} passHref>
								<MotionCol
									key={key}
									variants={item}
									className="h-[60vh] rounded-lg shadow-xl text-center cursor-pointer bg-red-500 text-white hover:bg-red-700 hover:scale-110"
									span={2}
									whileHover={{ scale: 1.1 }}
									whileTap={{ scale: 0.9 }}
								>
									{page.text}
								</MotionCol>
							</Link>
						);
					})}
				</MotionGrid>
			</AnimatePresence>
		</>
	);
};

export default Home;

